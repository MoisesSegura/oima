        var datasetStructure = function(dataset, labelName, valName){
            var labels = [];
            var values = [];
            for(var i = 0; i<dataset.length; i++){
                labels.push(dataset[i][labelName]);
                values.push(dataset[i][valName]);
            }
            return {
                labels: labels,
                values: values
            }
        }

        var createChart = function(data, id, type){
            var colors = [];
            var borderColor;
            var options;

            if(type === 'bar') {

                for (var i = 0; i < data.values.length; i++) {
                    var color = "#489F8C";
                    if (data.values[i]>= 100) {
                        color = "#489F8C";
                    } else if (data.values[i] >= 50 && data.values[i] < 100) {
                        color = "#9BDCCE"

                    } else if (data.values[i] < 50) {
                        color = "#BAECE1"
                    }

                    colors[i] = color;

                }
                borderColor = "rgba(0,0,0,0)";
                options = {
                    legend: {
                        display: true
                    },
                    scales: {
                        yAxes: [{
                            ticks: {
                                beginAtZero: true
                            }
                        }]
                    }
                }

            } else if (type === 'line') {
                colors = ['rgba(0,0,0,0)'];
                borderColor = '#489F8C';
                options = {
                    legend: {
                        display: false
                    },
                    scales: {
                        yAxes: [{
                            ticks: {
                                beginAtZero: true
                            }
                        }]
                    }
                }
